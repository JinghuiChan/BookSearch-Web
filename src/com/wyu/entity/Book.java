package com.wyu.entity;

public class Book {
	private int book_id;
	private String book_name;
	private int bookType_id;
	private int publish_id;
	private int bookAddress_id;
	private int book_sum;
	private int book_price;
	private String book_number;
	private String book_isbn;
	
	public Book() {
		
	}

	public int getBook_id() {
		return book_id;
	}

	public void setBook_id(int book_id) {
		this.book_id = book_id;
	}

	public String getBook_name() {
		return book_name;
	}

	public void setBook_name(String book_name) {
		this.book_name = book_name;
	}

	public int getBookType_id() {
		return bookType_id;
	}

	public void setBookType_id(int bookType_id) {
		this.bookType_id = bookType_id;
	}

	public int getPublish_id() {
		return publish_id;
	}

	public void setPublish_id(int publish_id) {
		this.publish_id = publish_id;
	}

	public int getBookAddress_id() {
		return bookAddress_id;
	}

	public void setBookAddress_id(int bookAddress_id) {
		this.bookAddress_id = bookAddress_id;
	}

	public int getBook_sum() {
		return book_sum;
	}

	public void setBook_sum(int book_sum) {
		this.book_sum = book_sum;
	}

	public int getBook_price() {
		return book_price;
	}

	public void setBook_price(int book_price) {
		this.book_price = book_price;
	}

	public String getBook_number() {
		return book_number;
	}

	public void setBook_number(String book_number) {
		this.book_number = book_number;
	}

	public String getBook_isbn() {
		return book_isbn;
	}

	public void setBook_isbn(String book_isbn) {
		this.book_isbn = book_isbn;
	}
	
	
}
